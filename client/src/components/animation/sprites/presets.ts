import { CAT_SPRITE_MAP, CAT_RUNNER } from './catPresets';

export type CharacterTheme = 'default' | 'cat';

const THEME_MAPS: Record<CharacterTheme, Record<string, SpritePreset>> = {
  default: SPRITE_PRESET_MAP,
  cat: CAT_SPRITE_MAP,
};

const THEME_FALLBACKS: Record<CharacterTheme, SpritePreset> = {
  default: RUNNER,
  cat: CAT_RUNNER,
};

export function getSpritePreset(type: string, theme: CharacterTheme = 'default'): SpritePreset {
  const map = THEME_MAPS[theme] || THEME_MAPS.default;
  const fallback = THEME_FALLBACKS[theme] || RUNNER;
  return map[type.toLowerCase()] || fallback;
}
